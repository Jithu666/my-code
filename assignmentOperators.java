public class assignmentOperators {
    public static void main(String[] args) {

        int a = 10, b;

        b = a;

        System.out.println(b);
        System.out.println(a+=b);
        System.out.println(a-=b);
        System.out.println(a*=b);
        System.out.println(a/=b);
        System.out.println(a%=b);

    }
}
